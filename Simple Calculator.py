class calculator:
    a=10
    b=20
    def sum(self):
        print(self.a+self.b)
    def sub(self):
        print(self.a-self.b)
    def mul(self):
        print(self.a*self.b)
    def div(self):
        print(self.a/self.b)
obj=calculator()
obj.sum()
obj.sub()
obj.mul()
obj.div()
